package com.cyanogenmod.cmparts.intents;

import android.content.BroadcastReceiver;
import android.content.Context;
import android.content.Intent;
import android.content.SharedPreferences;
import android.preference.PreferenceManager;
import android.text.TextUtils;

import com.cyanogenmod.cmparts.activities.UIActivity;
import com.cyanogenmod.cmparts.utils.SurfaceFlingerUtils;

public class BootReceiver extends BroadcastReceiver {
    @Override
    public void onReceive(Context context, Intent intent) {
        SharedPreferences prefs = PreferenceManager.getDefaultSharedPreferences(context);
        String renderEffect = prefs.getString(UIActivity.RENDER_EFFECT_PREF, null);

        if (renderEffect != null && !TextUtils.equals(renderEffect, "0")) {
            SurfaceFlingerUtils.setRenderEffect(context, Integer.valueOf(renderEffect));
        }
    }
}
